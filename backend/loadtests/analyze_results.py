#!/usr/bin/env python3
"""Analyze Locust staged CSV results and emit SLO-oriented summary.

Expected input files are generated by `backend/loadtests/run_stages.sh`:
  backend/loadtests/results/stage_<users>_stats.csv
"""

from __future__ import annotations

import csv
import glob
import json
import os
from dataclasses import dataclass, asdict


@dataclass
class EndpointStat:
    endpoint: str
    request_count: int
    failure_count: int
    failure_rate: float
    p50_ms: float
    p95_ms: float
    p99_ms: float


@dataclass
class StageSummary:
    users: int
    total_requests: int
    total_failures: int
    error_rate: float
    p95_read_max_ms: float
    p95_write_max_ms: float
    slowest_by_p99: list[EndpointStat]


READ_HINTS = ("/search", "/documents", "/dashboard", "/audit", "/health", "list", "detail")
WRITE_HINTS = ("upload", "create", "post", "approve", "reject", "retry", "act", "link", "unlink")


def _to_int(value: str | None) -> int:
    try:
        return int(float(value or 0))
    except ValueError:
        return 0


def _to_float(value: str | None) -> float:
    try:
        return float(value or 0)
    except ValueError:
        return 0.0


def _classify_endpoint(name: str) -> str:
    lowered = (name or "").lower()
    if any(hint in lowered for hint in WRITE_HINTS):
        return "write"
    if any(hint in lowered for hint in READ_HINTS):
        return "read"
    return "read"


def parse_stage_stats(path: str) -> StageSummary:
    basename = os.path.basename(path)
    users = int(basename.split("_")[1])

    rows: list[EndpointStat] = []
    total_requests = 0
    total_failures = 0
    p95_read_max = 0.0
    p95_write_max = 0.0

    with open(path, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            name = row.get("Name") or ""
            if not name or name == "Aggregated":
                continue

            request_count = _to_int(row.get("Request Count"))
            failure_count = _to_int(row.get("Failure Count"))
            median = _to_float(row.get("Median Response Time"))
            p95 = _to_float(row.get("95%"))
            p99 = _to_float(row.get("99%"))

            failure_rate = (failure_count / request_count) if request_count else 0.0

            stat = EndpointStat(
                endpoint=name,
                request_count=request_count,
                failure_count=failure_count,
                failure_rate=failure_rate,
                p50_ms=median,
                p95_ms=p95,
                p99_ms=p99,
            )
            rows.append(stat)

            total_requests += request_count
            total_failures += failure_count

            endpoint_kind = _classify_endpoint(name)
            if endpoint_kind == "read":
                p95_read_max = max(p95_read_max, p95)
            else:
                p95_write_max = max(p95_write_max, p95)

    rows.sort(key=lambda item: item.p99_ms, reverse=True)
    slowest = rows[:10]

    return StageSummary(
        users=users,
        total_requests=total_requests,
        total_failures=total_failures,
        error_rate=(total_failures / total_requests) if total_requests else 0.0,
        p95_read_max_ms=p95_read_max,
        p95_write_max_ms=p95_write_max,
        slowest_by_p99=slowest,
    )


def main():
    result_files = sorted(glob.glob("backend/loadtests/results/stage_*_stats.csv"))
    if not result_files:
        print("No Locust stats CSV files found under backend/loadtests/results")
        raise SystemExit(1)

    summaries = [parse_stage_stats(path) for path in result_files]

    output = {
        "stages": [
            {
                **asdict(stage),
                "slowest_by_p99": [asdict(item) for item in stage.slowest_by_p99],
                "targets": {
                    "read_p95_under_2000_ms": stage.p95_read_max_ms < 2000,
                    "write_p95_under_5000_ms": stage.p95_write_max_ms < 5000,
                    "error_rate_under_point_1_percent": stage.error_rate < 0.001,
                },
            }
            for stage in summaries
        ]
    }

    print(json.dumps(output, indent=2))


if __name__ == "__main__":
    main()
